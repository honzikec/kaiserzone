    <p>Overall, Hot Desking could make employees feel underappreciated, demotivated; and also make the employer look like a cheepskate.</p>
    <h2>
      How 'bout a Hybrid?
    </h2>
    <p>We established that Hot Desking has some advantages and also considerable disadvantages. But what should employer do if he really wants some of the pros without the cons?</p>
    <p>Of course, if they're just after the cost benefits and don't care about the job satisfaction of their employees, they can just establish Hot Desking and be happy...</p>
    <p>However, if they are after nourishing the knowlege-sharing collaborating atmosphere, breaking the routine and motivating their employees, there's a solution. The benefit of cutting costs turns to a con of probably suffering more costs, though.</p>
    <p>
      This solution could be called a "hybrid". It consists of keeping the traditional workplace
      <strong>and</strong> setting up a dedicated facility with spare desks that can be used by autonomous teams to collaborate; individual employees searching for a break of their routine; mentoring sessions; and other noble purposes.
    </p>
    <h2>Wrapping Up</h2>
    <p>In my opinion, Hot Desking as a company wide setup sucks hard. The cons heavily outweigh the pros; I might be biased by being a somehow conservative introvert though. Being as it may, I believe that Hot Desking as an option for those who want it when they want it can definitely be very benefitial both for productivity and job satisfaction.</p>
    <p>As for cutting costs, please cut them somewhere else - maybe fire some management?</p>
