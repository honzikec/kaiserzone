    <h1>
      Hot Desking - A Hot Take
    </h1>
    <div class="intro">
      <p>Hot Desking is a recent phenomenon. More and more workplaces tend to use this set up, where the employees don't have an assigned workspace and "catch" whatever desk they can get ahold of.</p>
      <p>The concept of Hot Desking is certainly an appealing way to save costs and effectively allocate resources. But how about the employees and their recognition and job satisfaction?</p>
    </div>
    <h2>
      What is Hot Desking?
    </h2>
    <blockquote cite="https://www.officespacesoftware.com/blog/hotddesking-101-what-is-hot-desking">
      The term is said to have derived from the term “hot racking,” a concept referring to the practice of sailors with different shifts sharing the same bunk.
    </blockquote>
    <p>In a nutshell, Hot Desking is an office organisation system where employees share workstations. Either between shifts or, commonly in tech, freely on a "first comes, first served" basis.</p>
    <p>Why commonly in tech? Simply because tech companies usually offer flexible hours, meaning a slim chance all the employees will meet at the office at the same time.</p>
    <h2>Hot Desking and Job Satisfaction</h2>
    <p>I think we can all agree that embracing Hot Desking is making a statement about the company culture. The culture has, of course, a big impact on job attitude and job satisfaction.</p>
    <p>How does a person feel in such environment? Well that depends on multiple factors:</p>
    <ul>
      <li>
        <strong>Age.</strong> It is said that younger employees usually enjoy said set up more than their older colleagues.
      </li>
      <li>
        <strong>Personality type.</strong> Of course, an extrovert will be more likely to enjoy this kind of workspace and an introvert might find himself in an unpleasant position.
      </li>
      <li>
        <strong>Type of job.</strong> While it can encourage collaboration and good atmosphere among, say, designers in a creative agency; for accountants or call center employees, the effect could be negative overall.
      </li>
      <li>
        <strong>Size and layout of the office space.</strong> In some cases, there just can happen that there will be more employees than desks at one point. Imagine what would happen to your job morale if you found yourself not having a desk left after comming to the office.
      </li>
    </ul>
    <h3>Keeping Things "Spicy"</h3>

    <blockquote cite="https://www.opensourcedworkplace.com/news/psychological-impact-of-hot-desking">
      People tend to work better and faster when they are less bored and constrained.
    </blockquote>

    <p>It is said that Hot Desking can mean more conversation between employees who would not normally meet and talk. It also encourages collaboration, facilitates work of self-organized teams, and eliminates routine and boredom.</p>
    <p>As I previously mentioned, Hot Desking is usually suitable for younger employees. There is another dimension to that: as it encourages collaboration, it allows the young and less experienced workers to absorb knowlege of their senior coworkers, learn from them, and be mentored.</p>
    <p>And of course, a little excitement goes a long way. A boring job means less job satisfaction and less job involvement; Hot Desking can "spice up" the job at least a little.</p>
    <h3>The Demotivational</h3>
    <blockquote cite="http://www.bbc.co.uk/guides/zgjmtfr">
      The US creative agency that helped pioneer hot desking in the 90s abandoned it a decade later, bringing back set desks after staff rebelled.
    </blockquote>
    <p>A lot of people need a sense of identity to be able to like their job and feel valuable in the company; not having their assigned spot might give them the sense of not being valued, therefore undermining their job morale.</p>
    <p>As I also mentioned above, there are introverts among us. I'm one of those. We just want to get our work done; we don't like interruptions and unnecessary social involvement. And we certainly don't like the overwhelming feeling of anxiousness when desperately searching for a free desk in a distant corner.</p>
    <p>And then there's privacy. Some people just don't work well without a sense of privacy. In fact, a lack of it can give them an impression of not being valued. That, of course, along with the distraction of an open workspace, can negatively affect effectivity.</p>
